x = 5

while x >= 0:
    x -= 1
    print(x)

print("End")
        
# Iteration | x start | x >= 0 | x end
#     1          5       True      4
#     2          4       True      3
#     3          3       True      2
#     4          2       True      1
#     5          1       True      0
#     6          0       True     -1 
#     7         -1       False  
