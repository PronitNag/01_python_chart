# List indexing.
my_list = ["a", "b", "c"]
print(my_list[0])
print(my_list[1])
print(my_list[2])

# Append an element.
numbers = [1, 2, 3]
numbers.append(4)
print(numbers)

# Insert an element.
numbers = [1, 2, 4]
numbers.insert(2, 3)
print(numbers)

# Remove an element.
colors = ["blue", "green", "purple"]
colors.remove("purple")
print(colors)

# Find the index of an element.
index = colors.index("green")
print(index)
