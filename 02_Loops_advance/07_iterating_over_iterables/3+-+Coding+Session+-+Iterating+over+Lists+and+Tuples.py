# Example 1: Iterate over list.

numbers = [1, 2, 3, 4, 5]

for number in numbers:
    print(number)

# Example 2: Different data types.

my_list = [[1, 2, 3], "Hello", (7, 8)]

for elem in my_list:
    print(elem)

# Example 3: Iterate over nested lists.

my_list = [[1, 2, 3], [4, 5, 6], [7, 8]]

for nested_list in my_list:
    print(nested_list)
